class KGameAssets {
  /// MAIN CHARACTER
  /// Characters:
  /// ```String
  /// Default is Ninja Frog
  /// Mask Dude
  /// Pink Man
  /// Virtual Guy
  /// ```
  static String mainCharacter({
    String? character = "Ninja Frog",
    KenumPlayerStates? playerState = KenumPlayerStates.idle,
  }) {
    switch (playerState) {
      case KenumPlayerStates.idle:
        return 'assets/images/game_images/Main Characters/$character/Idle (32x32).png';
      case KenumPlayerStates.hit:
        return 'assets/images/game_images/Main Characters/$character/Hit (32x32).png';
      case KenumPlayerStates.fall:
        return 'assets/images/game_images/Main Characters/$character/Fall (32x32).png';
      case KenumPlayerStates.jump:
        return 'assets/images/game_images/Main Characters/$character/Jump (32x32).png';
      case KenumPlayerStates.run:
        return 'assets/images/game_images/Main Characters/$character/Run (32x32).png';
      case KenumPlayerStates.wallJump:
        return 'assets/images/game_images/Main Characters/$character/Wall Jump (32x32).png';
      case KenumPlayerStates.doubleJump:
        return 'assets/images/game_images/Main Characters/$character/Double Jump (32x32).png';

      default:
        return 'assets/images/game_images/Main Characters/$character/Idle (32x32).png';
    }
  }

  // ENEMIES
  static String enemy({
    String? enemy = "Chicken",
    KenumPlayerStates? enemyState = KenumPlayerStates.idle,
  }) {
    switch (enemyState) {
      case KenumPlayerStates.run:
        return 'assets/images/game_images/Enemies/$enemy/Run (32x34).png';
      case KenumPlayerStates.hit:
        return 'assets/images/game_images/Enemies/$enemy/Hit (32x34).png';
      case KenumPlayerStates.idle:
        return 'assets/images/game_images/Enemies/$enemy/Idle (32x34).png';

      default:
        return 'assets/images/game_images/Enemies/$enemy/Idle (32x34).png';
    }
  }
}
